function isValidEmailFormat(email: string): boolean {
  const emailRegex = /^[a-zA-Z0-9.!#$%&'*+/=?^_`{|}~-]+@[a-zA-Z0-9](?:[a-zA-Z0-9-]{0,61}[a-zA-Z0-9])?(?:\.[a-zA-Z0-9](?:[a-zA-Z0-9-]{0,61}[a-zA-Z0-9])?)*$/;
  return emailRegex.test(email);
}

export async function validateEmail(email: string): Promise<EmailValidationResult> {
  if (!email || typeof email !== "string") {
    return { valid: false, error: "Email is required" };
  }

  const trimmed = email.trim().toLowerCase();

  if (!isValidEmailFormat(trimmed)) {
    return { valid: false, error: "Invalid email format" };
  }

  const domain = trimmed.split("@")[1];

  if (DISPOSABLE_DOMAINS.includes(domain)) {
    return { valid: false, error: "Temporary/disposable email addresses are not allowed" };
  }

  const apiKey = process.env.ABSTRACT_API_KEY;
  if (!apiKey) {
    console.warn("ABSTRACT_API_KEY not set — skipping email verification");
    return { valid: true };
  }

  try {
    const response = await fetch(
      `https://emailvalidation.abstractapi.com/v1/?api_key=${apiKey}&email=${encodeURIComponent(trimmed)}`
    );

    if (!response.ok) {
      console.error("Abstract API error:", response.status);
      return { valid: true };
    }

    const data = await response.json();

    const deliverability = data.deliverability;
    const qualityScore = data.quality_score;
    const isFormatValid = data.is_valid_format?.value;
    const isSmtpValid = data.is_smtp_valid?.value;
    const isMXValid = data.is_mx_found?.value;

    if (isFormatValid === false) {
      return { valid: false, error: "Invalid email format" };
    }

    if (isMXValid === false) {
      return { valid: false, error: "Email domain does not exist" };
    }

    if (isSmtpValid === false) {
      return { valid: false, error: "This email address does not exist" };
    }

    if (deliverability === "UNDELIVERABLE") {
      return { valid: false, error: "This email address is not valid" };
    }

    if (qualityScore !== null && qualityScore !== undefined && qualityScore < 0.5) {
      return { valid: false, error: "Email address appears to be fake or disposable" };
    }

    return {
      valid: true,
      deliverability,
      score: qualityScore,
    };
  } catch (error) {
    console.error("Email validation error:", error);
    return { valid: true };
  }
}
